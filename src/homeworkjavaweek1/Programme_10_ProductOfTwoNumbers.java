package homeworkjavaweek1;

/**
 * Write a java programme that takes two numbers as input and display the
 * product of two numbers.
 * Tets Data:
 * Input first number = 25
 * Input second number = 5
 * Expected Output : 25 x 5 = 125
 */

public class Programme_10_ProductOfTwoNumbers {
    public static void main(String[] args){
        //First Number Declaration
        int firstNumber = 25;
        //Second Number Declaration
        int secondNumber = 5;
        int produced = firstNumber * secondNumber;
        //Expected output
        System.out.println("first number" + " x " + secondNumber + " = " +  produced);

    }
}
